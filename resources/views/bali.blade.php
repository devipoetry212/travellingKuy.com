                        <div class="bg-white mb-3" style="padding: 30px;">
                            <h2 class="mb-3">Destinasi Instagrammable di Bali yang Wajib anda Kunjungi</h2>
                            <p>Siapa yang tidak menginginkan koleksi foto Instagram yang indah dan memuaskan secara visual sekaligus menampilkan petualangan yang menakjubkan? Bagi mereka yang merencanakan perjalanan liburan ke Bali, destinasi ini akan menjamin foto-foto yang anda ambil menjadi sangat indah!</p>
                    
                            <h4 class="mb-3">1. Seminyak</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/bali/seminyakbali.jpg">
                            <p>Seminyak merupakan area belanja, makan, dan hiburan mewah yang terletak tak jauh dari pantai Kuta. Area ini dianggap sebagai destinasi liburan paling eksklusif di pulau, dan penuh dengan resort mewah dan butik terkemuka. Anda akan menemukan berbagai spa untuk memanjakan diri, beragam restoran mewah di pulau, dan beberapa galeri seni fantastis yang mendukung kesenian lokal. Ada atmosfer pesta meriah dan banyak tempat hiburan malam di Seminyak, tetapi cenderung lebih tenang dan mewah dibandingkan dengan area lain di Bali yang lebih ramai dengan anak muda. Selain belanja, salah satu daya tarik utama area Seminyak adalah Pantai Seminyak.</p>
                            <h5 class="mb-3">2. Labuan Bajo</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/bali/labuanbajo.jpg">
                            <p>Labuan Bajo merupakan sebuah surga tersembunyi yang ada di Indonesia bagian timur. Desa ini terletak di Kecamatan Komodo, Kabupaten Manggarai Barat, Provinsi Nusa Tenggara Timur yang berbatasan langsung dengan Nusa Tenggara Barat dan dipisahkan oleh Selat Sape. Labuan Bajo adalah salah satu dari lima Destinasi Super Prioritas yang sedang dikembangkan di Indonesia. Destinasi ini merupakan gerbang menuju Taman Nasional Komodo yang menyimpan keindahan alam yang menakjubkan dan hewan purba yang mendunia. Mulai dari hewan endemik komodo di Pulau Rinca dan Pulau Komodo, deretan pulau eksotis, keragaman hayati bawah laut, hingga pantai aduhai. Menikmati pemandangan langit senja juga bisa menjadi cara lain menikmati Labuan Bajo. Spot strategis untuk menikmati keindahan senja ini berada tak jauh dari Bandar Udara Internasional Komodo.</p>
                            <h4 class="mb-3">3. Pura Besakih</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/bali/purabesakihbali.jpg">
                            <p>Pura Besakih adalah pura terbesar dan dianggap paling suci di Bali. Ramai dikenal sebagai “Pura Ibu”, pura ini terletak di lereng Gunung Agung di sisi sebelah timur pulau. Gunung Agung sebenarnya merupakan salah satu gunung berapi terbesar di pulau, dan merupakan titik tertinggi Bali, yakni 3.031 mdpl. Kompleks Pura Besakih, yang sebenarnya berada di lereng sebelah selatan gunung, terdiri dari 20 pura-pura lain yang tersebar di enam tingkat undakan. Pura ini dibangun lebih dari 1000 tahun yang lalu, dan menjadi situs penting dalam kehidupan beragama masyarakat Bali saat ini, serta menjadi salah satu tempat wisata terbaik di Bali. Lebih dari 70 upacara keagamaan dilangsungkan di sini setiap tahunnya.</p>
                            <h5 class="mb-3">4. Pantai Kuta</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/bali/pantaikutabali.jpg">
                            <p>Air terjun spektakuler ini berada di bagian utara pulau, sekitar 2,5 jam berkendara dari Denpasar, dan seringkali dianggap sebagai air terjun paling indah di Bali. Air terjun ini sebenarnya merupakan kumpulan dari tujuh air terjun yang berbeda. Air terjun yang jatuh dari tebing yang tinggi dan dikelilingi tumbuhan hijau menghasilkan foto yang memesona. Pencahayaan terbaik untuk foto dimulai sejak pagi menjelang siang hingga siang menjelang sore. Untuk sampai ke air terjun Sekumpul memerlukan waktu 30 menit hingga 2 jam perjalanan menuruni jalan setapak yang akan membawa Anda melintasi pedesaan, sawah terasering, dan hutan tropis. Dipungut biaya masuk sebesar IDR 20.000, dan banyak pengunjung yang memilih untuk menyewa pemandu lokal.</p>
                            <h4 class="mb-3">5. Pantai Pandawa</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/bali/pantaipandawabali.png">
                            <p>Pantai Pandawa adalah sepotong surga kecil di besarnya surga dunia Bali. Pantai pasir putih ini tersembunyi dan jalan masuknya diapit tebing tinggi menuju air biru yang jernih. Pantai ini terletak di Desa Kutuh dan berada di tengah-tengah antara Uluwatu dan Nusa Dua di Bukit Peninsula pulau, sekitar 30 menit berkendara dari keduanya. Jalan menuju pantai melewati enam patung yang ditempatkan di dalam cekungan yang dibuat di tebing. Setiap patung merepresentasikan karakter dalam epos Hindu, Mahabharata, termasuk lima bersaudara yang dikenal sebagai Pandawa – yang menginspirasi nama Pantai Pandawa.</p>
                            <h5 class="mb-3">6. Istana Ubud (Puri Saren Agung)</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/bali/istanaubudbali.jpg">
                            <p>Puri Saren Agung, atau yang lebih dikenal sebagai Istana Ubud, terletak tepat di seberang Pasar Seni Ubud, menjadikannya mudah untuk dikunjungi sekaligus. Dibangun sekitar tahun 1800 hingga 1823, istana ini sebelumnya merupakan rumah bagi keluarga kerajaan dan tetap berdiri sebagai salah satu landmark khas Bali hingga saat ini. Bangunan ini merupakan salah satu arsitektur khas Bali, dan kompleks istana ini merupakan rumah bagi taman yang hijau. Pencinta budaya, sejarah, dan arsitektur akan sangat senang mampir ke sini. Bagian depan Istana Ubud buka untuk umum, dan tidak dipungut biaya masuk sama sekali. </p>
                            <h4 class="mb-3">7. Pantai Padang Padang</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/bali/PadangBeachbali.jpg">
                            <p>Pantai Padang Padang adalah destinasi tropis sesungguhnya. Terletak di Desa Pecatu tidak jauh dari Uluwatu, pantai kecil tapi menawan ini berada di teluk yang berbentuk setengah lingkaran dan terbagi menjadi dua dengan air berwarna biru di satu sisi dan tebing hijau di sisi lainnya. Dipungut biaya masuk sebesar IDR 15.000 untuk dewasa dan IDR 10.000 untuk anak-anak, serta masing-masing IDR 5.000 untuk parkir mobil dan IDR 2.000 untuk parkir motor. Di pantai, Anda bisa menyewa atau membeli apa pun yang Anda butuhkan untuk bersenang-senang di pantai dari beragam pedagang yang ada, termasuk kursi pantai, payung, makanan, dan minuman. Pantai Padang Padang terkenal khususnya untuk pemecah ombaknya, yang juga merupakan rival bagi banyak titik berselancar paling populer di Bali.</p>
                            <h5 class="mb-3">8. Nusa Dua</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/bali/nusa2bali.jpg">
                            <p>Nusa Dua adalah lokasi paling mewah di Bali. Terletak di sisi selatan pulau, resort pantai mewah ini merupakan rumah bagi vila eksklusif, spa mewah, pantai pribadi, pusat belanja, dan bahkan lapangan golf. Jika Anda bersedia menghabiskan sedikit lebih banyak anggaran liburan Anda, makan Anda bisa benar-benar memanjakan diri dan menikmati waktu bebas dari keramaian. Plus, Nusa Dua tidak jauh dari bandara, memudahkannya untuk jadi pemberhentian pertama atau terakhir dalam rencana liburan Anda di Bali. Lokasi ini sangat cocok untuk bulan madu dan merupakan salah satu tempat wisata terbaik di Bali. Daya tarik utama di Nusa Dua adalah Pantai Nusa Dua.</p>
