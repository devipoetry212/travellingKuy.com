                            <h2 class="mb-3">Mau Road Trip Sepanjang Pulau Jawa? Jangan Lewatin Spot Wisata Menarik ini!</h2>
                            <p>Kalian pengin enggak sih ngerasain serunya road trip #DiIndonesiaAja? Dikelilingi oleh segudang keindahan alam yang ciamik, wajar aja kalau kalian penasaran sama rasanya menempuh perjalanan panjang bareng temen-temen atau keluarga. Kalian udah ada rencana buat bikin itu semua jadi kenyataan? Cobain deh serunya road trip  sepanjang pulau Jawa! Pokoknya spot-spot wisata ini bisa bikin road trip kalian  jadi makin spesial deh! Yuk, ah langsung aja cari tahu tentang tempatnya di bawah ini! </p>
                    
                            <h4 class="mb-3">1. Pantai Anyer</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/jawa/pantaianyer.jpg">
                            <p>Di awal road trip, yuk santai-santai dulu di salah satu primadona provinsi Banten ini! Terbentang di pesisir barat pulau Jawa, Pantai Anyer merupakan destinasi yang wajib kamu kunjungi saat memulai road trip. Pemandangan lautnya yang elok, pantai pasir putihnya yang luas, dan suasananya yang menenangkan, dijamin bakal bikin mood kamu dan temen-temen atau keluarga jadi bahagia sepanjang perjalanan menuju Jakarta dan Jawa Barat.</p>
                            <h5 class="mb-3">2. Pasar Batik Trusmi</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/jawa/batiktrusni.jpeg">
                            <p>Setelah memasuki Jawa Barat dan berada di Kota Cirebon, jangan sampe lupa buat dateng ke Pasar Batik Trusmi yang berlokasi di Kecamatan Plered! Di pasar yang legendaris ini, Sobat Pesona bisa nemuin berbagai macam kerajinan batik dengan motif yang berbeda, mulai dari motif Megamendung khas Cirebon sampai motif khas Pekalongan. Harga yang ditawarkan di sini juga cukup terjangkau, lho! Jadi bersahabat banget sama kantong! </p>
                            <h4 class="mb-3">3. Taman Wisata Air Panas Guci</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/jawa/airpanasguci.jpg">
                            <p>Sesampainya di kawasan Jawa Tengah, enggak ada salahnya kalian buat melepas penat di Taman Wisata Air Panas Guci yang berlokasi di Kabupaten Tegal. Terletak di lereng Gunung Slamet, pemandian air panas yang satu ini dipercaya oleh warga sekitar dapat memberikan manfaat yang sangat baik bagi kesehatan. Selain memiliki khasiat, suasana asri dari Taman Wisata Air Panas Guci juga dijamin bakal bikin jiwa dan pikiran kalian jadi lega. </p>
                            <h5 class="mb-3">4. Pantai Sigandu</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/jawa/pantaisigandu.jpg">
                            <p>Ada lagi nih satu pantai yang mesti kalian datengin setelah melepas penat di Taman Wisata Air Panas Guci! Namanya Pantai Sigandu. Pantai dengan pemandangan yang elok ini terletak di timur laut Pekalongan, tepatnya di pesisir utara Kabupaten Batang, Jawa Tengah. Di pantai yang cocok buat dijadiin spot foto ini, kalian bisa menikmati indahnya matahari terbenam sambil menyantap makanan khas setempat, seperti nasi megono dan lauk gimbal rebon, di warung-warung yang ada di sekitar pantai.</p>
                            <h4 class="mb-3">5. Eling Bening</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/jawa/elingbening.jpg">
                            <p>Sehabis bersenang-senang di Pantai Sigandu, ini saatnya kalian buat melanjutkan perjalanan ke Semarang! Nah, selagi melewati Semarang, coba deh main ke tempat yang bernama Eling Bening di Kecamatan Bawen. Berdiri di atas perbukitan Ngrawan, Eling Bening menawarkan panorama alam yang memesona. Dari sini, bisa menyaksikan kemegahan Gunung Merbabu serta pegunungan Telomoyo dan Andong sambil berendam santai di kolam renang yang tersedia. </p>
                            <h5 class="mb-3">6. Umbul Sidomukti</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/jawa/umbulsidomukti.jpg">
                            <p>Di barat laut dari Kecamatan Bawen, kalian juga bisa menemukan sebuah tempat wisata yang pemandangannya enggak kalah keren sama Eling Bening! Berlokasi di Kecamatan Bandungan, tepatnya di lereng Gunung Ungaran, Umbul Sidomukti menawarkan pemandangan pegunungan hijau yang ciamik lengkap dengan beberapa fasilitas menarik. Di sini, bisa menikmati sensasi sedap dari secangkir kopi hangat yang disajikan di sini sambil ditemani indahnya matahari terbenam di ufuk barat! </p>
                            <h4 class="mb-3">7. Monumen Simpang Lima Gumul</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/jawa/gumul.jpg">
                            <p>Setelah asyik menikmati pemandangan alam di sekitar Semarang, yuk mampir dulu ke Kediri di Jawa Timur! Kota yang satu ini terkenal dengan monumen-monumennya yang megah, salah satunya berada di kawasan Tugurejo. Monumen yang diberi nama Simpang Lima Gumul ini pas banget buat dijadiin spot foto-foto, Sobat Pesona! Memiliki luas 804 meter persegi, tinggi 25 meter, serta terdapat tangga setinggi 3 meter, monumen menakjubkan ini dibangun berdasarkan hari jadi Kota Kediri yang jatuh pada 25 Maret 804 Masehi, lho! </p>
                            <h5 class="mb-3">8. Bromo Pananjakan</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/jawa/bromo.jpg">
                            <p>Sehabis berfoto ria di Monumen Simpang Lima Gumul, waktunya Sobat Pesona untuk berkunjung ke kawasan paling menakjubkan yang ada di Jawa Timur! Apalagi kalo bukan Taman Nasional Bromo Tengger Semeru yang terletak di timur Kota Malang! Di taman nasional yang sudah sangat populer ini Sobat Pesona bakal disuguhin sama pemandangan alam yang sungguh mengagumkan. Coba deh dateng ke tiga puncak penanjakan yang terdapat di sekitar sini! Dari ketiga puncak ini, Sobat Pesona bakal dapet kesempatan buat ngeliat pesona Gunung Bromo, Batok, Widodaren, Watangan, Kursi, serta Semeru dengan jelas dari kejauhan. </p>
