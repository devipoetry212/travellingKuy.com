                            <h2 class="mb-3">Destinasi Instagrammable di Bali yang Wajib anda Kunjungi</h2>
                            <p>Siapa yang tidak menginginkan koleksi foto Instagram yang indah dan memuaskan secara visual sekaligus menampilkan petualangan yang menakjubkan? Bagi mereka yang merencanakan perjalanan liburan ke Bali, destinasi ini akan menjamin foto-foto yang anda ambil menjadi sangat indah!</p>
                    
                            <h4 class="mb-3">1. Pesisir Kalianda</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/sumatra/kalianda.jpg">
                            <p>Lampung merupakan salah satu provinsi di Sumatera yang terkenal akan keindahan pantai-pantainya. Jika kamu berkunjung ke Lampung melalui jalur laut dan berlabuh di Pelabuhan Bakauheni, kamu bisa melipir ke Pesisir Kalianda dimana disepanjang pesisir tersebut kamu bisa mengunjungi pantai-pantai indah seperti Pantai Tapak Kera, Pantai Kedu, Pantai Marina, Pantai Batu Lapis, Pantai Merak Belantun, Pantai Sebalang, dan pantai-pantai indah lainnya. Selain itu, di Lampung juga masih terdapat berbagai pantai indah lainnya yang bisa kamu kunjungi mulai dari Pantai Tanjun Setia, Pantai Pasir Putih, Pantai Krui dan juga Pantai Gigi Hiu.</p>
                            <h5 class="mb-3">2. Danau Ranau</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sumatra/danauranau.png">
                            <p>Di perbatasan provinsi Lampung dan Sumatera Selatan, kamu bisa menjumpai destinasi wisata di Sumatera selanjutnya, yakni Danau Ranau. Objek wisata di Sumatera satu ini merupakan danau terbesar kedua di pulau Sumatera setelah Danau Toba. Selain pemadangannya yang masih sangat asri, daya tarik lainnya dari destinasi wisata di Sumatera satu ini adalah udaranya yang sangat sejuk dan menyegarkan sehingga kamu akan dibuat betah menikmati panoramanya. </p>
                            <h4 class="mb-3">3. Bukit Khayangan</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/sumatra/bukit.jpg">
                            <p>Dari Sumatera Selatan, kamu bisa melanjutkan perjalanan road trip ke Jambi dan menjumpai Bukit Khayangan, objek wisata di Sumatera yang berada pada ketinggian 1.500 meter di atas laut. Keberadaan kabut dan awan yang turun ke dataran tendah membuatmu seperti tengah berada di atas awan. Dari objek wisata di Sumatera ini juga kamu bisa memandang lebih dekat panorama Gunung Kerinci yang menakjubkan. Untuk kamu yang mencari suasana berbeda yang mampu menyegarkan pikiran, destinasi wisata di Sumatera satu ini bisa jadi pilihan terbaik. </p>
                            <h5 class="mb-3">4. Lembah Harau</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sumatra/lembah.jpg">
                            <p>Objek wisata di Sumatera selanjutnya adalah Lembah Harau yang berlokasi di Payakumbuh, Sumatera Barat. Kawasan cagar alam yang mengapit Kelok Sembilan ini menyajikan pemadangan indah ngarai dengan tebing-tebing granit dan cadar yang sangat eksotis. Semakin indah karena di kawasan destinasi wisata Sumatera ini terdapat air terjun serta keberadaan rumah-rumah adat Minangkabau yang menambah unik pemandanganmu saat singgah ke Lembah Harau. </p>
                            <h4 class="mb-3">5. Ngarai Sianok</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/sumatra/ngaraisianok.jpg">
                            <p>Masih berada di Sumatera Barat, kamu bisa melanjutkan petualanganmu ke Ngarai Sianok. Destinasi wisata di Sumatera ini terletak di perbatasan Bukittingi, Kabupaten Agam dan kerap dijuluki Lembah Pendiam karena suasana di kawasan ini sangat sunyi sehingga kamu bisa merasakan atmosfer tenang dan damau. Waktu terbaik untuk mengunjungi destinasi wisata di Sumatera ini adalah sore hari. Selain karena udara sejuknya yang semakin teduh, kamu juga bisa menikmati panorama matahari terbenam yang sangat indah di tempat ini. </p>
                            <h5 class="mb-3">6. Danau Maninjau</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sumatra/maninjau.jpg">
                            <p>Sumatera Barat memang kaya akan destinasi wisata, objek wisata di Sumatera populer selanjutnya adalah Danau Maninjau. Meskipun perjalanan ke destinasi wisata di Sumatera ini cukup menantang, namun pemandangan indah yang memukau di Danau Maninjau dijamin akan membuat perjalanan tersebut terbayar lunas. </p>
                            <p>Danau Maninjau (berarti "pemandangan" atau "peninjauan" dalam bahasa Minangkabau) adalah sebuah danau kaldera di kecamatan Tanjung Raya, Kabupaten Agam, provinsi Sumatera Barat, Indonesia. Danau ini terletak sekitar 140 kilometer (87 mi) sebelah utara Kota Padang, ibu kota Sumatera Barat, 36 kilometer (22 mi) dari Bukittinggi, 27 kilometer (17 mi) dari Lubuk Basung, ibu kota Kabupaten Agam.</p>
                            <h4 class="mb-3">7. Ombak Bono</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/sumatra/ombakbono.jpg">
                            <p><i>Spot surfing</i> di Indonesia memang sudah sangat tersohor di dunia. Berbeda dengan spot surfing yang pada umumnya berada di pantai, destinasi wisata di Sumatera selanjutnya ini menawarkan sensasi berselancar di sungai!. Berada di provinsi Riau, tepatnya di Sungai Kampar, kamu bisa menemukan fenomena alam ombak Bono dimana pertemuan arus air tawar dan air asin yang menghasilkan gelombak yang sangat cocok untuk surfing. Untuk merasakan langsung pengalaman surfing di objek wisata Sumatera satu ini, pastikan kamu mengunjunginya pada bulan purnama pertama bulan Agustus dimana fenomena ombak Bono ini terjadi. </p>
                            <h5 class="mb-3">8. Danau Toba</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sumatra/danautoba.jpg">
                            <p>Danau Toba yang terletak di provinsi Sumatera Utara merupakan destinasi wisata yang memadukan antara keindahan alam serta keunikan tradisi budaya. Baru-baru ini danau toba masuk sebagai satu dari lima Destinasi Super Prioritas (DSP) oleh Kemenparekraf .Keindahan alam Danau Toba seringkali menjadi ikon pariwisata Indonesia. Danau Toba juga telah ditetapkan sebagai Unesco Global Geopark dalam sidang ke-209 Dewan Eksekutif UNESCO di Paris, Prancis pada 2 Juli 2020 lalu. Jika anda ingin berwisata ke danau toba, tetapi bingung mau menuju destinasi Objek Wisata Danau Toba yang mana, kamu dapat membaca rekomendasi tempat wisata yang berada di sekitar danau toba ini.</p>
                            <h4 class="mb-3">9. Pulau Samosir</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/sumatra/samosir.jpg">
                            <p>Samosir adalah sebuah pulau vulkanik yang terletak di tengah Danau Toba di Provinsi Sumatera Utara. Sebuah pulau dalam pulau dengan ketinggian 1.000 meter di atas permukaan laut menjadikan pulau ini menjadi sebuah pulau yang menarik perhatian para turis. Tuktuk adalah pusat konsentrasi turis di Pulau Samosir. Dari Parapat, Tuktuk dapat dihubungkan dengan feri penyeberangan. Selain perhubungan air, Pulau Samosir juga dapat dicapai lewat jalan darat melalui Pangururan yang menjadi tempat di mana Pulau Samosir dan Pulau Sumatra terhubung. Pulau Samosir sendiri terletak dalam wilayah Kabupaten Samosir yang baru dimekarkan pada tahun 2003 dari bekas Kabupaten Toba Samosir. Di pulau ini juga terdapat beberapa danau kecil, dua diantaranya dikenal sebagai daerah wisata yaitu Danau Sidihoni dan Danau Aek Natonang yang mendapat julukan "danau di atas danau". </p>
                            <h5 class="mb-3">10. Pantai Lhok Me</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sumatra/lhokmee.jpg">
                            <p>Pantai Pasir Putih Lhok Mee merupakan salah satu pantai yang ada di Provinsi Aceh. Pantai ini memilki keunikan tersendiri yang membedakannya dengan pantai lain yang ada di Aceh. Di Pantai Lhok Mee kamu dapat melihat keindahan pohon yang tumbuh berjejer di dalam permukaan laut. Masyarakat sekitar biasa menyebutnya dengan pohon Geurumbang. Selain itu, kamu juga dapat melihat akar-akar bakau yang menjulang di tepi pantai. Air laut di Pantai Lhok Mee sendiri bisa pasang-surut seketika. Ketika air laut tersebut pasang, pasir pantai yang putih tersebut berpadu warnanya dengan air laut yang biru nan bening. Namun, ketika air laut kembali surut. Akar-akar bakau tersebut akan muncul satu per satu seperti ujung tombak. </p>
