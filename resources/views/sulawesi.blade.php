                            <h2 class="mb-3">Destinasi Wisata Paling Terkenal di Sulawesi</h2>
                            <p>Pulau Sulawesi terletak di dekat Pulau Kalimantan dan Maluku, merupakan pulau ke-4 terbesar di Indonesia, ada 6 propinsi di Pulau Sulawesi yakni Sulawesi Barat, Sulawesi Utara, Sulawesi Tengah, Sulawesi Selatan, Sulawesi Tenggara, dan Gorontalo. Jika Anda menyukai wisata alam, maka pulau ini sangat cocok untuk Anda kunjungi, ingin tahu wisata alam apa saja yang terkenal  di Sulawesi? Berikut ada destinasi wisata pilihan yang wajib Anda datangi bila jalan-jalan ke Sulawesi! </p>
                    
                            <h4 class="mb-3">1. Taman Laut Bunaken</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/sulawesi/bunaken.jpg">
                            <p>Jika ke Menado tanpa ke Taman Laut Bunaken, rasanya kurang lengkap. Bunaken luasnya mempunyai luas 8,08 kmÂ². Keindahan ekosistem lautnya telah menghipnotis para pecinta diving dan snorkeling dari seluruh dunia untuk menyaksikan sendiri surga di bawah permukaan laut bunaken. Terdapat 29 titik untuk menyelam di taman laut bunaken, dan dari ke 29 titik tersebut 12 diantaranya yang paling sering dikunjungi. Bunaken memiliki beragam biota laut yang luar biasa seperti 70 karang yang berbeda, lima spesies penyu. Serta banyak spesies ikan dan para duyung yang nyaris punah bahkan hiu putih dan karang hitam, serta barracuda dan bahkan buaya air asin!</p>
                            <h5 class="mb-3">2. Tana Toraja</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sulawesi/toraja.jpg">
                            <p>Tana Toraja terkenal dengan budayanya yang unik, ritual dan kuburan kunonya. Salah satu tradisi menarik yang selalu menyedot wisatawan asing adalah tradisi pemakaman toraja. Pemakaman toraja sangat khas, dan hanya dilakukan oleh kalangan bangsawan. Tradisi pemakaman toraja berlangsung selama berhari-hari, dan salah satu ritual di dalamnya adalah pembantaian kerbau. Karena keunikan budayannya inilah maka Tana Toraja menjadi salah satu tempat wisata di sulawesi yang terkenal hingga ke seluruh dunia.</p>
                            <h4 class="mb-3">3. Taman Nasional Windu</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/sulawesi/tnwindu.jpg">
                            <p>Terletak di Palu, Taman Nasional Windu akan memberikan Anda pengalaman tersendiri, dimulai dengan menaiki gunung di jalan yang tergolong ekstrim. Anda dapat mengalami menjadi yang pertama dalam menemukan sebuah hutan yang belum dijelajahi yang kaya dengan flora dan fauna. Anda mungkin menemukan kupu-kupu, burung enggang, monyet, ular dan juga banyak bunga dan tanaman liar. </p>
                            <h5 class="mb-3">4. Wakatobi</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sulawesi/wakatobi.jpg">
                            <p>Wakatobi terletak di Sulawesi Tenggara dan terdiri dari beberapa pulau yang menakjubkan. Bahkan banyak yang berpendapat bahwa taman nasional wakatobi merupakan salah satu surga bawah laut paling indah di muka bumi. Salah satu jurnalis selam berpendapat Wakatobi adalah tempat menyelam paling indah di dunia. Sebuah ekspedisi dari Inggris pada tahun 2005 menemukan bahwa 750 dari 850 spesies koral dunia terdapat di Wakatobi. Bisa anda bayangkan bagaimana kekayaan laut daerah ini.</p>
                            <h4 class="mb-3">5. Kepulauan Togean</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/sulawesi/togean.jpg">
                            <p>Kepulauan Togean ibarat untaian permata indah di Teluk Tomini, Sulawesi Tengah.Keistimewaan sekaligus daya tarik adalah bawah lautnya. Terumbu karang yang masih sangat subur sehingga menjadi rumah idaman ratusan spesies ikan. Umumnya masih alami. Karenanya tidak heran menjadi incaran pencinta wisata bahari. Sebutlah wisata menyelam, snorkling, mancing, atau sekedar santai menikmati pantai pasir putih yang masih alami. Ternyata para peneliti pun tertarik datang di Kepulauan Togean. Terlebih ada beberapa jenis karang baru yang justru di temukan di sini. Yang menarik dari ratusan spesies ikan, terdapat jenis Pracheilinus togeanensis dan Ecsenius sp yang hanya di temukan di Kepulauan Togean. Berarti Kepulauan Togean menjadi surganya para peneliti kekayaan bawah laut. </p>
                            <h5 class="mb-3">6. Pantai Tanjung Bira</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sulawesi/tanjungbira.jpg">
                            <p>Tanjung bira terletak di daerah Sulawesi Selatan. Lokasi khususnya adalah Kabupaten Bulukumba yang jaraknya sangat jauh, sekitar 200 km dari pusat ibukota Makassar. Sehingga tanjung bira bisa dikatakan terletak di ujung selatan daratan Sulawesi Selatan. Pantai Tanjung bira juga menawarkan matahari terbit dan terbenam yang langsung dilihat dan tenggelam menuju ke lautan lepas. Karena laut di tanjung bira merupakan laut lepas. Keindahan ini tidak akan didapatkan di tempat lain, terutama di bagian lain di Indonesia. Ciri khas yang utama lainnya Tanjung Bira memiliki pasir pantai yang sangat halus, bahkan sangat kecil sehingga terasa seperti tepung. Banyak laut dan pantai yang tentu saja tidak memiliki ciri khas seperti ini. </p>
                            <h4 class="mb-3">7. Taman Laut Takabonerate</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/sulawesi/takabonerate.jpg">
                            <p>Taman Laut Taka Bonerate merupakan destinasi cantik yang ada di Sulawesi Selatan dan terkenal sebagai tempat terbaik untuk menyelam, snorkeling, dan wisata bahari. Topografinya di kawasan yang menarik di mana terdapat atol yang terdiri dari gugusan pulau-pulau gosong dan karang serta terumbu karang yang luas.
                            Pada saat air surut, terlihat jelas daratan kering dan diselingi genangan air yang membentuk kolam-kolam kecil. Sangat cantik!. Taka Bonerate memiliki 244 jenis moluska, 261 jenis terumbu karang dari 17 famili, 295 jenis ikan karang aneka warna, dan daratan terumbu karang sebesar 500 kilometer persedi di atol seluas 220.000 hektar. </p>
                            <h5 class="mb-3">8. Benteng Somba Opu</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sulawesi/somba.jpg">
                            <p>Benteng Somba Opu adalah salah satu wisata sejarah di Sulawesi Selatan. Benteng ini merupakan komplek wisata di bawah naungan dinas pariwisata Pemda Kabupaten Gowa. Kini Benteng Sumba Opu menjadi objek bersejarah yang didalamnya terdapat beberapa bangunan adat Sulawesi Selatan mewakili Sugu Bugis, Makassar, Mandar dan Toraja. Selain itu terdapat sebuah meriam dengan panjang 9 m dan berat 9.500 kg serta sebuah museum yang berisi peninggalan bersejarah Kerajaan Gowa.</p>
                            <h5 class="mb-3">9. Bukit Kasih Kanonang</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/sulawesi/kanonang.jpg">
                            <p>Bukit Kasih adalah salah satu tempat pariwisata di Provinsi Sulawesi Utara. Di Bukit Kasih ini terdapat monumen. Bukit Kasih ini terletak sekitar 55 km arah selatan Manado, tepatnya di Desa Kanonang, Kabupaten Minahasa. Bukit Kasih ini merupakan bukit belerang yang masih alami. Di tempat ini perasaan kasih wisatawan akan digugah.</p>
                            <p>Bukit Kasih dibangun pada tahun 2002 sebagai pusat keagamaan di mana semua pemeluk agama bisa berkumpul dan beribadat di bukit tropis yang rimbun dan berkabut. Dinamakan Bukit Kasih karena tempat ini menimbulkan rasa keharmonisan antar umat beragama. Terdapat lima rumah ibadah di Bukit Kasih yaitu Gereja Katolik, Gereja Kristen, Kuil Buddha, Masjid, dan Candi Hindu yang berada di puncak kedua. Di puncak pertama kita bisa melihat sebuah salib putih yang tingginya mencapai 53 meter yang bisa dilihat dari Pantai Boulevard, Manado. Di tempat ini juga diyakini sebagai tempat asli nenek moyang suku Minahasa, Toar dan Lumimuut tinggal. </p>
