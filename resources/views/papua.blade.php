                            <h2 class="mb-3">7 Destinasi Wisata di Pulau Papua yang Indahnya Tiada Dua</h2>
                            <p>Indonesia bagian timur emang juara soal urusan alam dan budaya. Semua yang indah-indah ini dimiliki oleh tanah Papua, pulau terbesar kedua di dunia. Demi menjaga keasrian alamnya, Papua punya taman nasional terbesar di Asia Tenggara. Taman tersebut adalah Taman Nasional Lorentz, salah satu destinasi wisata andalan Papua.</p>
                            
                            <h4 class="mb-3">1. Danau Sentani</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/papua/danausentanipapua.jpg">
                            <p>Danau Sentani adalah danau terbesar di Papua yang memiliki panorama fantastis dan jadi kebanggaan masyarakat, yang terletak di Kabupaten Jayapura. Danau dengan luas 9.360 hektare ini merupakan sumber air dari 14 sungai besar dan sungai kecil. Tak hanya itu, danau ini pun juara dalam produksi ikannya yang bisa mencapai 1.823 ton/tahun. Dengan bentuk danau yang unik seperti amoeba, terdapat 22 pulau kecil yang menghiasi keindahan danau.</p>
                            <h5 class="mb-3">2. Pantai Base-G</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/papua/pantaibaseg.jpg">
                            <p>Pantai Base-G yang terletak di Kota Jayapura. Asal nama pantai ini berawal dari pasukan sekutu di Perang Dunia II yang menjadikan pantai ini sebagai basis militer. Nama lain dari pantai ini adalah Pantai Tanjung Ria. Jika anda main ke pantai ini, dijamin bakal merasa rileks karena deburan ombak pantai yang merdu dan tidak terlalu besar. Selain itu, pasir bersih yang membentang di pantai ini bikin pengen foto-foto terus! Pokoknya, anda tidak boleh melewati pantai ini jikake Papua</p>
                            <h4 class="mb-3">3. Taman Nasional Wasur</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/papua/TamanNasionalWasurpapua.jpg">
                            <p>Jalan-jalan ke Papua belum lengkap rasanya kalau belum bertemu fauna unik seperti burung kasuari. Kekayaan flora dan fauna Papua bisa Sobat Pesona temui di Taman Nasional Wasur yang terletak di Kabupaten Merauke. Dengan luas lahan sebesar 413.810 hektare, taman ini memiliki hutan sabana terluas di Indonesia, bahkan di Asia. Nah, kebanyakan fauna di taman ini adalah fauna migran dan tinggal di enam ekosistem yang berbeda. Kalau Sobat Pesona berkunjung ke sini, rasanya seperti lagi berpetualang!</p>
                            <h5 class="mb-3">4. Taman Nasional Lorentz</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/papua/lorentzpapua.jpg">
                            <p>Taman Nasional Lorentz di Papua pun memiliki kemegahan terbesar di Asia Tenggara dan diakui sebagai situs warisan dunia oleh UNESCO. Saking luasnya, 2.505.600 hektar lahan taman ini menempati 10 kabupaten di Papua. Enggak heran, pintu masuk taman nasional ini ada tiga kota berbeda, yaitu Timika, Wamena, dan Enarotali. Luas banget ya! Tak hanya itu, Taman Nasional Lorentz juga merupakan kawasan konservasi terluas dengan ekosistem terlengkap di Asia Pasifik. Fauna khasnya adalah cendrawasih ekor panjang, puyuh salju, dan kanguru pohon dingiso. </p>
                            <h4 class="mb-3">5. Lembah Baliem</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/papua/baliempapua.jpg">
                            <p>Pegunungan Jayawijaya adalah gunung tertinggi di Indonesia. Nah, kalau dari Pegunungan Jayawijaya, wajib juga mampir ke Lembah Baliem, yang berjarak 27 km dari Kota Wamena, Papua. Di lembah yang luasnya mencapai 1.600 km2 tersebut, terdapat 3 suku utama yang menempati, yaitu Suku Dani, Yali, dan Lani. Selain udara yang sejuk dan panorama asri yang bikin segar mata, daya tarik destinasi ini adalah rumah tradisional serta kebudayaan yang kental di antara masyarakat suku aslinya. Bahkan, ada festival budaya yang terkenal di sini, yaitu Festival Lembah Baliem.</p>
                            <h5 class="mb-3">6. Air Terjun Wafsarak</h5>
                            <img class="img-fluid w-50 float-right ml-4 mb-2" src="assets/img/des/papua/AirTerjunWafsarakpapua.jpg">
                            <p>Air Terjun Wafsarak di Biak Utara, Papua Barat adalah jawabannya! Panorama air terjun setinggi 10 meter ini seperti tempat pelepas penat di tengah hutan yang asri. Suara deras air terjun pun bisa Sobat Pesona dengar dari pinggir jalan karena lokasinya yang mudah diakses. Air di kolam air terjun sangat jernih sehingga cocok untuk berenang dan bermain air bersama anak-anak. Kalau Sobat Pesona bawa rombongan keluarga, berkunjung ke lokasi wisata ini adalah pilihan yang wajib! </p>
                            <h4 class="mb-3">7. Raja Ampat</h4>
                            <img class="img-fluid w-50 float-left mr-4 mb-2" src="assets/img/des/papua/rajaampatpapua2.jpg">
                            <p>Siapa yang belum kenal jagoan wisatanya Papua Barat, sang Raja Ampat? Berkat gugusan pulau bak kura-kura yang menghiasi lautan Raja Ampat serta cantiknya taman bawah laut yang mengagumkan, popularitas destinasi wisata ini sukses mendunia!. Berlokasi di Kabupaten Raja Ampat, Papua Barat, luas Kepulauan Raja Ampat mencapai 4,6 juta hektare! Area ini rumah bagi 75% spesies laut di dunia, lho! Surga bawah laut memang bukan sekadar julukan. Pokoknya, Raja Ampat memang pantas jadi yang pertama kali harus dikunjungi ke Papua.</p>
